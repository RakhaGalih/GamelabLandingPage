function showMenu(){
    document.getElementById('menu').style.display = 'block';
    document.getElementById('menu').style.opacity = '0.9';
}
function closeMenu(){
    document.getElementById('menu').style.display = 'none';
    document.getElementById('menu').style.opacity = '0';
}
